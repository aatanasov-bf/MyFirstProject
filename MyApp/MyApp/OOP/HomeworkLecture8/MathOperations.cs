namespace MyApp.OOP.HomeworkLecture8
{
    public class MathOperations
    {

        public int Add(int a, int b)
        {
            int result;
            result = a + b;
            return result;
        }

        public double Add(double a, double b, double c)
        {
            double result;
            result = a + b + c;
            return result;
        }

        public decimal Add(decimal a, decimal b, decimal c)
        {
            decimal result;
            result = a + b + c;
            return result;
        }
    }
}
